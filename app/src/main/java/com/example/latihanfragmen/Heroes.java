package com.example.latihanfragmen;

public class Heroes {
    private String heroName, heroContent;
    private int heroImage;

    public String getHeroName() {
        return heroName;
    }

    public void setHeroName(String heroName) {
        this.heroName = heroName;
    }

    public String getHeroContent() {
        return heroContent;
    }

    public void setHeroContent(String heroContent) {
        this.heroContent = heroContent;
    }

    public int getHeroImage() {
        return heroImage;
    }

    public void setHeroImage(int heroImage) {
        this.heroImage = heroImage;
    }
}
